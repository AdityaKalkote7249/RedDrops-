              <h5 class="card1-title">Who Can Not Donate?</h5>
              <p class="card1-text">

              <h6>Certain individuals may be deemed ineligible to donate blood due to various factors. Some common
                reasons for not being able to donate blood include:</h6>
              <br>
              <b> 1.Age:</b> There are specific age requirements for blood donation. For instance, donors who are below
              the minimum age requirement (usually 17 or 19 years old, depending on gender) may not be eligible.<br>
              <b> 2. Health Conditions:</b> Certain health conditions may disqualify individuals from donating blood.
              Conditions such as active infections, heart diseases, cancers, blood clotting disorders, and certain
              chronic conditions may prevent someone from being able to donate.<br>
              <b>3. Medications and Treatments:</b> Some medications and treatments may affect eligibility. For example,
              individuals taking certain medications, such as certain types of antibiotics or anticoagulants, may be
              temporarily ineligible.<br>
              <b>4. Travel:</b> Recent travel to specific regions or countries with a high prevalence of certain
              infectious diseases may lead to temporary deferral from blood donation. This is done to minimize the risk
              of transmitting infections through donated blood.<br>
              <b>5. Recent Body Modification:</b> Undergoing certain procedures, such as getting a tattoo or piercing
              within a specified period, may result in temporary deferral due to concerns about potential bloodborne
              infection.<br>
              <b>6. Pregnancy:</b> Pregnancy can temporarily disqualify individuals from donating blood to protect the
              health of both the donor and the fetus.<br>
              </p>

