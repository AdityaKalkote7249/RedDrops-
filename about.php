              <p class="card1-text">
              <h6>Blood donation eligibility requirements can vary depending on the country and organization. Generally,
                individuals who meet the following criteria may be eligible to donate blood:</h6>
              <br>
              <b>1.Age:</b> Donors are typically required to be of a certain age. For instance, in most states of the
              US, male donors must be at least 17 years old, while female donors must be at least 19 years old.<br><br>

              <b>2. Weight and Height:</b> Donors are usually required to meet specific weight and height criteria. For
              example, male donors may need to be at least 5'1" tall and weigh at least 130 lbs, while female donors may
              need to be at least 5'5" tall and weigh at least 150 lbs.<br><br>

              <b>3.Health:</b> Donors must be in good health and feeling well on the day of donation. They should not
              have any acute illnesses, infections, or chronic conditions that could pose a risk to their own health or
              the safety of the recipient.<br><br>

              <b> 4.Medical History:</b> Donors may be asked about their medical history, including any previous or
              current medical conditions, surgeries, or medications they are taking. Certain medical conditions or
              medications may affect eligibility, but it depends on the specific circumstances.</p>

